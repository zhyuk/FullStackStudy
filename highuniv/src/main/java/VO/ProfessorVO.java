package vo;

public class ProfessorVO {
	private String professor_id;
	private String professor_pw;
	private String professor_name;
	private String professor_email;
	private int professor_ph;
	private String professor_status;

	public String getProfessor_id() {
		return professor_id;
	}

	public void setProfessor_id(String professor_id) {
		this.professor_id = professor_id;
	}

	public String getProfessor_pw() {
		return professor_pw;
	}

	public void setProfessor_pw(String professor_pw) {
		this.professor_pw = professor_pw;
	}

	public String getProfessor_name() {
		return professor_name;
	}

	public void setProfessor_name(String professor_name) {
		this.professor_name = professor_name;
	}

	public String getProfessor_email() {
		return professor_email;
	}

	public void setProfessor_email(String professor_email) {
		this.professor_email = professor_email;
	}

	public int getProfessor_ph() {
		return professor_ph;
	}

	public void setProfessor_ph(int professor_ph) {
		this.professor_ph = professor_ph;
	}

	public String getProfessor_status() {
		return professor_status;
	}

	public void setProfessor_status(String professor_status) {
		this.professor_status = professor_status;
	}

}
